#!/usr/bin/env node
/**
 * Generates `src/infrastructure/api/popularModels.ts` — a popularity-ranked list
 * of HuggingFace model ids for the search-bar autocomplete.
 *
 * WHY a bundled list: calling the Hub directly from the browser is unreliable in
 * production (its CloudFront edges intermittently drop the per-origin CORS
 * header), and we don't want a backend hop for search. So we fetch popular ids
 * here (server-side — no CORS) at author/maintainer discretion and ship the
 * result as a static asset the app filters in memory. No runtime network call.
 *
 * Run:  pnpm generate:models
 *
 * Every query keeps `library=transformers` so suggestions are loadable by the
 * backend introspector (without it, `sort=likes` surfaces diffusion models).
 * Popularity = downloads is the global ranking key; likes / trending / specific
 * maintainer orgs (e.g. unsloth) widen coverage.
 */

import { writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const HUB = "https://huggingface.co/api/models";
const TARGET = 1000;

const SOURCES = [
  "library=transformers&sort=downloads&direction=-1&limit=700",
  "library=transformers&sort=likes&direction=-1&limit=300",
  "library=transformers&sort=trendingScore&direction=-1&limit=150",
  "author=unsloth&library=transformers&sort=downloads&direction=-1&limit=250",
];

async function fetchSource(qs) {
  const res = await fetch(`${HUB}?${qs}`, { headers: { Accept: "application/json" } });
  if (!res.ok) throw new Error(`HF responded ${res.status}`);
  return res.json();
}

// id -> max downloads seen across sources (downloads is the global ranking key).
const downloadsById = new Map();

for (const qs of SOURCES) {
  try {
    const models = await fetchSource(qs);
    let added = 0;
    for (const m of models) {
      if (!m?.id) continue;
      const dl = typeof m.downloads === "number" ? m.downloads : 0;
      if (dl > (downloadsById.get(m.id) ?? -1)) downloadsById.set(m.id, dl);
      added += 1;
    }
    console.log(`  ${qs.split("&sort")[0].padEnd(40)} → ${added} models`);
  } catch (e) {
    console.warn(`  ${qs.split("&sort")[0].padEnd(40)} → SKIPPED (${e.message})`);
  }
}

const ids = [...downloadsById.entries()]
  .sort((a, b) => b[1] - a[1]) // most downloads first
  .slice(0, TARGET)
  .map(([id]) => id);

if (ids.length === 0) {
  console.error("No model ids fetched — aborting so the existing list isn't wiped.");
  process.exit(1);
}

const file = `// AUTO-GENERATED by scripts/generate-popular-models.mjs — do not edit by hand.
// Regenerate with:  pnpm generate:models   (last run: ${new Date().toISOString().slice(0, 10)})
//
// ${ids.length} popular HuggingFace transformers model ids, ranked by downloads.
// Powers the search-bar autocomplete entirely client-side (no network call).
export const POPULAR_MODELS: readonly string[] = [
${ids.map((id) => `  ${JSON.stringify(id)},`).join("\n")}
];
`;

const out = resolve(
  dirname(fileURLToPath(import.meta.url)),
  "../src/infrastructure/api/popularModels.ts",
);
writeFileSync(out, file);
console.log(`\nWrote ${ids.length} model ids → ${out}`);
